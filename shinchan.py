def bhuru_r_line(a,b,c,d):
    
    t.pendown()
    t.right(140)
    for _ in range(a):
        t.forward(20)
        t.right(d)
    t.right(10)
    t.forward(20)
    t.right(10)
    t.forward(20)
    t.right(20)
    for _ in range(b):
        t.forward(20)
        t.right(c)

def bhuru():
    t.penup()
    t.goto(-110,130)
